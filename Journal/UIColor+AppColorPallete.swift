import Foundation
import UIKit

extension UIColor {
    
    static func myBlueColor() -> UIColor {
        return UIColor(red: 0.130, green: 0.449, blue: 0.785, alpha: 1.00)
    }
    
    static func myGreenColor() -> UIColor {
        return UIColor(red :0.311, green: 0.785, blue: 0.271, alpha: 1.00)
    }
    
}